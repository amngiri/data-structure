package rotateLL;

public class rotateNode {
private int data;
private rotateNode next;
public rotateNode()
{
	next=null;
	data=0;
}
public int getData() {
	return data;
}
public void setData(int data) {
	this.data = data;
}
public rotateNode getNext() {
	return next;
}
public void setNext(rotateNode next) {
	this.next = next;
}

}
